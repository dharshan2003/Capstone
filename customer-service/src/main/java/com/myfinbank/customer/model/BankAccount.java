package com.myfinbank.customer.model;

import java.math.BigDecimal;

import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import jakarta.persistence.JoinColumn;
import jakarta.persistence.ManyToOne;
import jakarta.persistence.Table;

@Entity
@Table(name = "bank_account")
public class BankAccount {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @Column(name = "account_number", nullable = false, unique = true)
    private String accountNumber;

    @Column(name = "account_type", nullable = false)
    private String accountType; // e.g. SAVINGS, CURRENT

    @Column(name = "balance_amount", nullable = false)
    private BigDecimal balance = BigDecimal.ZERO;

    @Column(name = "status_flag", nullable = false)
    private String status = "ACTIVE"; // ACTIVE / INACTIVE

    @ManyToOne
    @JoinColumn(name = "customer_id", nullable = false)
    private CustomerProfile owner;

    public Long getId() { return id; }
    public void setId(Long id) { this.id = id; }

    public String getAccountNumber() { return accountNumber; }
    public void setAccountNumber(String accountNumber) { this.accountNumber = accountNumber; }

    public String getAccountType() { return accountType; }
    public void setAccountType(String accountType) { this.accountType = accountType; }

    public BigDecimal getBalance() { return balance; }
    public void setBalance(BigDecimal balance) { this.balance = balance; }

    public String getStatus() { return status; }
    public void setStatus(String status) { this.status = status; }

    public CustomerProfile getOwner() { return owner; }
    public void setOwner(CustomerProfile owner) { this.owner = owner; }
}
